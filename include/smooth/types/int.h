{
	typedef long		 Int;
	typedef signed long	 SignedInt;
	typedef unsigned long	 UnsignedInt;

	typedef char		 Byte;
	typedef signed char	 SignedByte;
	typedef unsigned char	 UnsignedByte;

	typedef short		 Short;
	typedef signed short	 SignedShort;
	typedef unsigned short	 UnsignedShort;

	typedef long		 Long;
	typedef signed long	 SignedLong;
	typedef unsigned long	 UnsignedLong;

	typedef char		 Int8;
	typedef signed char	 SignedInt8;
	typedef unsigned char	 UnsignedInt8;

	typedef short		 Int16;
	typedef signed short	 SignedInt16;
	typedef unsigned short	 UnsignedInt16;

	typedef long		 Int32;
	typedef signed long	 SignedInt32;
	typedef unsigned long	 UnsignedInt32;

	typedef __int64		 Int64;
	typedef signed __int64	 SignedInt64;
	typedef unsigned __int64 UnsignedInt64;
};

#endif
